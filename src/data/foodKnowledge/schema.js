export const createFoodRecord = ({
  id,
  name,
  aliases = [],
  category,
  defaultQuantity = 1,
  defaultUnit = 'piece',
  quantityUnits,
  icon = 'food-item',
  profile = 'pantryStaple',
  ...overrides
}) => {
  const profileValues = SHELF_LIFE_PROFILES[profile] || SHELF_LIFE_PROFILES.pantryStaple;

  return {
    id,
    name,
    aliases,
    category,
    defaultQuantity,
    defaultUnit,
    pantryShelfLife: profileValues.pantryShelfLife,
    refrigeratorShelfLife: profileValues.refrigeratorShelfLife,
    freezerShelfLife: profileValues.freezerShelfLife,
    idealStorageLocation: profileValues.idealStorageLocation,
    idealStorageTemperature: profileValues.idealStorageTemperature,
    storageTips: profileValues.storageTips,
    spoilageSigns: profileValues.spoilageSigns,
    canFreeze: profileValues.canFreeze,
    canRefreeze: profileValues.canRefreeze,
    quantityUnits: quantityUnits || [defaultUnit, 'g', 'kg', 'pack'],
    icon,
    ...overrides,
  };
};

export const validateFoodRecord = (food) =>
  FOOD_REQUIRED_FIELDS.every((field) => Object.prototype.hasOwnProperty.call(food, field)) &&
  Array.isArray(food.aliases) &&
  Array.isArray(food.spoilageSigns) &&
  Array.isArray(food.quantityUnits) &&
  typeof food.canFreeze === 'boolean' &&
  typeof food.canRefreeze === 'boolean';
